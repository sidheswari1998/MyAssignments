public class CheckNumberIsPositive {

	public static void main(String[] args) {
		
		int version = 10;
		
		if (version > 0) 
		{
			System.out.println(version + " is POSITIVE number!");
			
		}
		else 
		{
			System.out.println(version + " is NEGATIVE number!");
			
		}

	}

}
